#include "3-calc.h"

/**
 * op_add - function that adds
 * @a: first operand
 * @b: second operand
 * Return: returns sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - function that adds
 * @a: first operand
 * @b: second operand
 * Return: returns difference
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - function that adds
 * @a: first operand
 * @b: second operand
 * Return: returns product
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - function that adds
 * @a: first operand
 * @b: second operand
 * Return: returns quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - function that adds
 * @a: first operand
 * @b: second operand
 * Return: returns remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
